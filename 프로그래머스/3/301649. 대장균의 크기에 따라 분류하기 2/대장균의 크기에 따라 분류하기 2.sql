SELECT
    A.ID,
    CASE
        WHEN A.B <= 0.25 THEN 'CRITICAL'
        WHEN A.B <= 0.5 THEN 'HIGH'
        WHEN A.B <= 0.75 THEN 'MEDIUM'
        ELSE 'LOW'
    END
    AS COLONY_NAME
FROM
    (SELECT
        ID,
        PERCENT_RANK() OVER (ORDER BY SIZE_OF_COLONY DESC) AS B
    FROM
        ECOLI_DATA) AS A
ORDER BY
    ID;